SELECT FIRST_HALF.FLAVOR
FROM FIRST_HALF 
JOIN ICECREAM_INFO ON FIRST_HALF.FLAVOR = ICECREAM_INFO.FLAVOR
WHERE FIRST_HALF.TOTAL_ORDER > 3000 AND ICECREAM_INFO.ingredient_type = 'fruit_based'
ORDER BY FIRST_HALF.TOTAL_ORDER DESC
